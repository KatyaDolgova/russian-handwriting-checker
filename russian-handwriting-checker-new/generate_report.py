#!/usr/bin/env python3
"""Генератор отчёта дипломного проекта «РусЯзык AI»"""

from docx import Document
from docx.shared import Pt, Cm, RGBColor
from docx.enum.text import WD_ALIGN_PARAGRAPH, WD_LINE_SPACING
from docx.enum.table import WD_TABLE_ALIGNMENT
from docx.oxml.ns import qn
from docx.oxml import OxmlElement

FONT   = 'Times New Roman'
SZ     = Pt(14)
SZ_SM  = Pt(12)
INDENT = Cm(1.25)
LS     = Pt(21)   # 1.5 × 14pt


# ─────────────────────────────────────────────
#  Утилиты
# ─────────────────────────────────────────────

def _run(para, text, bold=False, italic=False, sz=None):
    run = para.add_run(text)
    run.font.name  = FONT
    run.font.size  = sz or SZ
    run.font.bold  = bold
    run.font.italic = italic
    run.font.color.rgb = RGBColor(0, 0, 0)
    return run


def _pf(para, align=WD_ALIGN_PARAGRAPH.JUSTIFY, first_indent=True,
        space_before=Pt(0), space_after=Pt(0)):
    pf = para.paragraph_format
    pf.alignment          = align
    pf.first_line_indent  = INDENT if first_indent else Pt(0)
    pf.line_spacing_rule  = WD_LINE_SPACING.MULTIPLE
    pf.line_spacing       = LS
    pf.space_before       = space_before
    pf.space_after        = space_after


def txt(doc, text, bold=False, italic=False, align=WD_ALIGN_PARAGRAPH.JUSTIFY,
        indent=True, sz=None, sb=Pt(0), sa=Pt(0)):
    para = doc.add_paragraph()
    _pf(para, align=align, first_indent=indent, space_before=sb, space_after=sa)
    if text:
        _run(para, text, bold=bold, italic=italic, sz=sz)
    return para


def h1(doc, text):
    """Заголовок главы: ПРОПИСНЫЕ, по центру, жирный, новая страница."""
    doc.add_page_break()
    para = doc.add_paragraph()
    _pf(para, align=WD_ALIGN_PARAGRAPH.CENTER, first_indent=False,
        space_before=Pt(0), space_after=Pt(18))
    _run(para, text.upper(), bold=True)
    return para


def h1_no_break(doc, text):
    """Заголовок главы без разрыва страницы (для Введения и Заключения)."""
    para = doc.add_paragraph()
    _pf(para, align=WD_ALIGN_PARAGRAPH.CENTER, first_indent=False,
        space_before=Pt(0), space_after=Pt(18))
    _run(para, text.upper(), bold=True)
    return para


def h2(doc, text):
    """Заголовок параграфа: с красной строки, без выделения."""
    para = doc.add_paragraph()
    _pf(para, align=WD_ALIGN_PARAGRAPH.JUSTIFY, first_indent=True,
        space_before=Pt(12), space_after=Pt(6))
    _run(para, text)
    return para


def blank(doc):
    txt(doc, '', indent=False)


def add_page_numbers(doc):
    for section in doc.sections:
        footer  = section.footer
        para    = footer.paragraphs[0]
        para.clear()
        para.alignment = WD_ALIGN_PARAGRAPH.CENTER
        run = para.add_run()
        run.font.name = FONT
        run.font.size = SZ
        for ftype, label in [('begin', None), (None, 'PAGE'), ('end', None)]:
            if ftype:
                el = OxmlElement('w:fldChar')
                el.set(qn('w:fldCharType'), ftype)
                run._r.append(el)
            else:
                el = OxmlElement('w:instrText')
                el.text = label
                run._r.append(el)


def make_table(doc, headers, rows, col_widths=None, header_bold=True):
    n_cols = len(headers)
    n_rows = len(rows) + 1
    table  = doc.add_table(rows=n_rows, cols=n_cols)
    table.style = 'Table Grid'

    # Заголовки
    for i, h in enumerate(headers):
        cell = table.rows[0].cells[i]
        cell.paragraphs[0].clear()
        p2   = cell.paragraphs[0]
        p2.alignment = WD_ALIGN_PARAGRAPH.CENTER
        r    = p2.add_run(h)
        r.font.name = FONT; r.font.size = SZ_SM; r.font.bold = header_bold

    # Данные
    for ri, row_data in enumerate(rows, start=1):
        for ci, val in enumerate(row_data):
            cell = table.rows[ri].cells[ci]
            cell.paragraphs[0].clear()
            p2   = cell.paragraphs[0]
            p2.alignment = WD_ALIGN_PARAGRAPH.LEFT
            r    = p2.add_run(val)
            r.font.name = FONT; r.font.size = SZ_SM

    if col_widths:
        for row in table.rows:
            for i, w in enumerate(col_widths):
                row.cells[i].width = w

    return table


# ─────────────────────────────────────────────
#  Инициализация документа
# ─────────────────────────────────────────────

def setup_doc():
    doc = Document()
    for section in doc.sections:
        section.left_margin   = Cm(3.0)
        section.right_margin  = Cm(1.5)
        section.top_margin    = Cm(2.0)
        section.bottom_margin = Cm(2.0)

    style = doc.styles['Normal']
    style.font.name = FONT
    style.font.size = SZ
    pf = style.paragraph_format
    pf.alignment         = WD_ALIGN_PARAGRAPH.JUSTIFY
    pf.line_spacing_rule = WD_LINE_SPACING.MULTIPLE
    pf.line_spacing      = LS
    pf.space_before      = Pt(0)
    pf.space_after       = Pt(0)
    pf.first_line_indent = INDENT
    return doc


