def title_page(doc):
    def cline(text, sz=14, bold=False):
        para = doc.add_paragraph()
        _pf(para, align=WD_ALIGN_PARAGRAPH.CENTER, first_indent=False)
        _run(para, text, bold=bold, sz=Pt(sz))

    def rline(text, sz=14):
        para = doc.add_paragraph()
        _pf(para, align=WD_ALIGN_PARAGRAPH.RIGHT, first_indent=False)
        _run(para, text, sz=Pt(sz))

    cline('[НАЗВАНИЕ УЧЕБНОГО ЗАВЕДЕНИЯ]')
    cline('Факультет [название факультета]')
    cline('Кафедра [название кафедры]')

    for _ in range(7): blank(doc)

    cline('ДИПЛОМНЫЙ ПРОЕКТ', sz=16, bold=True)
    blank(doc)
    cline('Тема: «РусЯзык AI - веб-приложение для автоматической\n'
          'проверки письменных работ по русскому языку»', sz=16, bold=True)

    for _ in range(7): blank(doc)

    rline('Выполнила: студентка ___ курса\nгруппы ___\n[Фамилия Имя Отчество]')
    blank(doc)
    rline('Руководитель: [учёная степень, должность]\n[Фамилия Имя Отчество]')

    for _ in range(8): blank(doc)

    cline('[Город], 2026')


